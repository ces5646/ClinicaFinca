
package Modelo;

public class HabitoToxico {
    private String IdHabitoToxico;
    private String HabitoToxico;

    public HabitoToxico() {
    }

    public HabitoToxico(String IdHabitoToxico, String HabitoToxico) {
        this.IdHabitoToxico = IdHabitoToxico;
        this.HabitoToxico = HabitoToxico;
    }

    public String getIdHabitoToxico() {
        return IdHabitoToxico;
    }

    public void setIdHabitoToxico(String IdHabitoToxico) {
        this.IdHabitoToxico = IdHabitoToxico;
    }

    public String getHabitoToxico() {
        return HabitoToxico;
    }

    public void setHabitoToxico(String HabitoToxico) {
        this.HabitoToxico = HabitoToxico;
    }
    
    
}
