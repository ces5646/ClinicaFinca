
package Modelo;

public class Traumatologico {
    private String IdTraumatologico;
    private String Traumatologico;

    public Traumatologico() {
    }

    public Traumatologico(String IdTraumatologico, String Traumatologico) {
        this.IdTraumatologico = IdTraumatologico;
        this.Traumatologico = Traumatologico;
    }

    public String getIdTraumatologico() {
        return IdTraumatologico;
    }

    public void setIdTraumatologico(String IdTraumatologico) {
        this.IdTraumatologico = IdTraumatologico;
    }

    public String getTraumatologico() {
        return Traumatologico;
    }

    public void setTraumatologico(String Traumatologico) {
        this.Traumatologico = Traumatologico;
    }
    
    
    
}
